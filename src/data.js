// Fonction pour analyser le contenu avec Compromise
function analyzeContentWithCompromise(text) {
  const doc = nlp(text);
  
  // Extraire les entités nommées
  const places = doc.places().out('array');
  const people = doc.people().out('array');
  const organizations = doc.organizations().out('array');
  
  // Extraire les concepts clés
  const nouns = doc.nouns().out('array');
  const verbs = doc.verbs().out('array');
  const adjectives = doc.adjectives().out('array');
  
  // Extraire les thèmes par patterns
  const themes = [];
  
  // Détecter les thèmes géographiques
  if (places.length > 0) {
    themes.push('géographie');
  }
  
  // Détecter les thèmes de mouvement social
  const socialKeywords = ['grève', 'strike', 'révolution', 'mouvement', 'syndicat', 'travailleur'];
  if (socialKeywords.some(keyword => text.toLowerCase().includes(keyword))) {
    themes.push('mouvement social');
  }
  
  // Détecter les thèmes technologiques
  const techKeywords = ['technologie', 'numérique', 'digital', 'informatique', 'software'];
  if (techKeywords.some(keyword => text.toLowerCase().includes(keyword))) {
    themes.push('technologie');
  }
  
  // Détecter les thèmes urbains
  const urbanKeywords = ['ville', 'urbain', 'vernaculaire'];
  if (urbanKeywords.some(keyword => text.toLowerCase().includes(keyword))) {
    themes.push('urbanisme');
  }
  
  // Détecter les thèmes de surveillance
  const surveillanceKeywords = ['surveillance', 'privacy', 'vie privée'];
  if (surveillanceKeywords.some(keyword => text.toLowerCase().includes(keyword))) {
    themes.push('surveillance');
  }
  
  // Détecter les thèmes de libre/open source
  const libreKeywords = ['libre', 'open source', 'open'];
  if (libreKeywords.some(keyword => text.toLowerCase().includes(keyword))) {
    themes.push('libre');
  }
  
  // Détecter les thèmes de collectif
  const collectifKeywords = ['collectif', 'communauté', 'coopératif'];
  if (collectifKeywords.some(keyword => text.toLowerCase().includes(keyword))) {
    themes.push('collectif');
  }
  
  // Détecter les thèmes spécifiques
  if (text.toLowerCase().includes('afrique') || text.toLowerCase().includes('africain')) {
    themes.push('afrique');
  }
  
  if (text.toLowerCase().includes('lightning strike') || text.toLowerCase().includes('grève éclair')) {
    themes.push('grève éclair');
  }
  
  if (text.toLowerCase().includes('néo-vernaculaire')) {
    themes.push('néo-vernaculaire');
  }
  
  return {
    places,
    people,
    organizations,
    nouns: nouns.slice(0, 10), // Limiter pour éviter le bruit
    verbs: verbs.slice(0, 10),
    adjectives: adjectives.slice(0, 10),
    themes: [...new Set(themes)], // Dédupliquer
    wordCount: doc.wordCount(),
    confidence: doc.confidence()
  };
}

// Fonction pour calculer la similarité sémantique avec Compromise
function calculateCompromiseSimilarity(text1, text2) {
  const doc1 = nlp(text1);
  const doc2 = nlp(text2);
  
  // Extraire les mots significatifs (noms, verbes, adjectifs)
  const words1 = new Set([
    ...doc1.nouns().out('array'),
    ...doc1.verbs().out('array'),
    ...doc1.adjectives().out('array')
  ].map(w => w.toLowerCase()));
  
  const words2 = new Set([
    ...doc2.nouns().out('array'),
    ...doc2.verbs().out('array'),
    ...doc2.adjectives().out('array')
  ].map(w => w.toLowerCase()));
  
  // Calculer l'intersection et l'union
  const intersection = new Set([...words1].filter(x => words2.has(x)));
  const union = new Set([...words1, ...words2]);
  
  // Similarité Jaccard
  const jaccardSimilarity = intersection.size / union.size;
  
  // Bonus pour les entités nommées communes
  const places1 = new Set(doc1.places().out('array').map(p => p.toLowerCase()));
  const places2 = new Set(doc2.places().out('array').map(p => p.toLowerCase()));
  const commonPlaces = new Set([...places1].filter(x => places2.has(x)));
  
  const people1 = new Set(doc1.people().out('array').map(p => p.toLowerCase()));
  const people2 = new Set(doc2.people().out('array').map(p => p.toLowerCase()));
  const commonPeople = new Set([...people1].filter(x => people2.has(x)));
  
  // Score final
  let score = jaccardSimilarity;
  score += commonPlaces.size * 0.1;
  score += commonPeople.size * 0.1;
  
  return Math.min(1.0, score);
}

// Fonction de recherche sémantique avec Compromise
export function semanticSearch(query, data, threshold = 0.05) {
  // Analyser la requête avec Compromise
  const queryAnalysis = analyzeContentWithCompromise(query);
  
  // Calculer les scores de similarité
  const results = data.map(entry => {
    // Créer le texte combiné pour l'entrée
    const entryText = `${entry.titre || ''} ${entry.texte || ''} ${entry.auteur || ''} ${entry.tag || ''}`.trim();
    
    if (entryText) {
      // Analyser le contenu de l'entrée avec Compromise
      const entryAnalysis = analyzeContentWithCompromise(entryText);
      
      // Calculer la similarité sémantique
      const similarityScore = calculateCompromiseSimilarity(query, entryText);
      
      // Score basé sur l'analyse Compromise
      let score = similarityScore;
      
      // Bonus pour thèmes communs
      const commonThemes = queryAnalysis.themes.filter(theme => 
        entryAnalysis.themes.includes(theme)
      );
      score += commonThemes.length * 0.2;
      
      // Bonus pour entités communes
      const commonPlaces = queryAnalysis.places.filter(place => 
        entryAnalysis.places.includes(place)
      );
      score += commonPlaces.length * 0.15;
      
      const commonPeople = queryAnalysis.people.filter(person => 
        entryAnalysis.people.includes(person)
      );
      score += commonPeople.length * 0.15;
      
      // Bonus pour mots significatifs communs
      const commonNouns = queryAnalysis.nouns.filter(noun => 
        entryAnalysis.nouns.includes(noun)
      );
      score += commonNouns.length * 0.05;
      
      return {
        ...entry,
        similarityScore: Math.min(1.0, score)
      };
    }
    
    return {
      ...entry,
      similarityScore: 0
    };
  });
  
  // Filtrer et trier
  return results
    .filter(entry => entry.similarityScore > threshold)
    .sort((a, b) => b.similarityScore - a.similarityScore);
}

// Fonction d'embedding simple (gardée pour compatibilité)
function calculateSimpleEmbedding(text) {
  const words = text.toLowerCase()
    .replace(/[^wsàáâäçèéêëìíîïñòóôöùúûü]/g, ' ')
    .split(/s+/)
    .filter(w => w.length > 2);
  
  const embedding = new Array(50).fill(0);
  
  words.forEach((word, i) => {
    for (let j = 0; j < word.length && j < 50; j++) {
      embedding[j] += word.charCodeAt(j % word.length);
    }
  });
  
  const magnitude = Math.sqrt(embedding.reduce((sum, val) => sum + val * val, 0));
  return embedding.map(val => val / magnitude);
}
